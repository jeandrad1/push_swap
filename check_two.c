#include "push_swap.h"

//Continuation of the movements function ft_exec_sort.
//It executes the movement ra.
//It rotates the stack a.

void	ft_ra_check(t_list **stack_a)
{
	t_list	*last;
	t_list	*first;

	if (*stack_a == NULL || (*stack_a)->next == NULL)
		return ;
	last = *stack_a;
	first = *stack_a;
	while (last->next != NULL)
		last = last->next;
	*stack_a = first->next;
	first->next = NULL;
	last->next = first;
	return ;
}

